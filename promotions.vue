<template>
    <div> <!-- without an outer container div this component template will not render -->
        <loader v-if="!dataLoaded"></loader>
        <transition name="fade">
            <div v-if="dataLoaded" v-cloak>
        		<div class="page_header" v-if="pageBanner" v-bind:style="{ backgroundImage: 'url(' + pageBanner.image_url + ')' }">
        			<div class="site_container">
        				<div class="header_content caps">
        					<h1>{{ $t("promos_page.promos_header") }}</h1>
        				</div>
        			</div>
        		</div>
        		<div class="site_container page_content">
        			<div >
        				
    					<div class="promo_container" v-if="promos" v-for="promo in promos">
    					    <div class="promo_img" v-if="locale=='en-ca'" v-lazy:background-image="promo.image_url"></div>
    					    <div class="promo_img" v-else v-lazy:background-image="promo.promo_image2_url_abs"></div>
    					    <div class="promo_content">
    					        <p class="promo_title" v-if="promo.store">{{ promo.store.name }}</p>
    					        <p class="promo_title" v-else>{{ property.name }}</p>
    					        <h3 class="center caps" v-if="locale=='en-ca'">{{ promo.name_short }}</h3>
    							<h3 class="center caps" v-else>{{ promo.name_short_2 }}</h3>
    							<router-link :to="'/promotions/'+ promo.slug">
								   <div class="promo_learn_more animated_btn">{{ $t("promos_page.read_more") }}</div>
    						    </router-link>
    					    </div>
    					</div>

        			</div>
        			<div class="row" v-else>
        				<div class="col-md-12">
        					<p>{{$t("promos_page.no_promo_message")}}</p>
        				</div>
        			</div>
        			<div class="row">
                        <div class="col-md-12">
                            <button class="animated_btn event_load_more" v-if="!noMoreEvents" @click="handleButton">Load More</button>
                            <p v-if="noEvents">No More Promotions</p>
                        </div>
                    </div>
        			<!--<div class="row">-->
        			<!--	<div class="col-md-12">-->
        			<!--		<paginate-links for="promos" :async="true" :limit="3" :show-step-links="true"></paginate-links>-->
        			<!--	</div>-->
        			<!--</div>-->
        		</div>
	        </div>
	    </transition>
	</div>
</template>

<script>
    define(["Vue", "vuex", "moment", "moment-timezone", "vue-moment", "vue-meta", "vue-lazy-load", "vue-paginate"], function(Vue, Vuex, moment, tz, VueMoment, Meta, VueLazyload, VuePaginate) {
        Vue.use(Meta);
        Vue.use(VueLazyload);
        Vue.use(VuePaginate);
        return Vue.component("promos-component", {
            template: template, // the variable template will be injected
            props:['locale'],
            data: function() {
                return {
                    dataLoaded: false,
                    pageBanner: null,
                    // promos : null,
                    // paginate: ['promos']
                    
                    events: [],
                    moreEvents: [],
                    moreEventsFetched: false,
                    noMoreEvents: false,
                    noEvents: false
                }
            },
            created() {
                this.loadData().then(response => {
                    var temp_repo = this.findRepoByName('Promotions Banner');
                    if (temp_repo) {
                        try {
                            this.pageBanner = temp_repo.images[0];
                        } catch(e) {
                            
                        }
                    } else {
                        this.pageBanner = { "image_url": "https://via.placeholder.com/1920x300" }
                    }
                    // console.log(this.pageBanner);
                    // this.promos = this.promotions;
                    // console.log(this.promos);
                    
                    this.handleButton();
                    this.dataLoaded = true;
                });
                
            },
            computed: {
                ...Vuex.mapGetters([
                    'property',
                    'timezone',
                    'findRepoByName',
                    'processedPromos'
                ]),
                promotions() {
                    var vm = this;
                    var temp_promo = [];
                    var temp_job = [];
                    _.forEach(this.processedPromos, function(value, key) {
                        value.name_short = _.truncate(value.name, { 'length': 21, 'separator': ' ' });
                        value.name_short_2 = _.truncate(value.name_2, { 'length': 21, 'separator': ' ' });
                        value.description_short = _.truncate(value.description, { 'length': 60, 'separator': ' ' });
                        value.description_short_2 = _.truncate(value.description_2, { 'length': 60, 'separator': ' ' });

                        if (_.includes(value.image_url, 'missing')) {
                            value.image_url = "https://placehold.it/1600x800/757575";
                        }
                        if (_.includes(value.promo_image2_url_abs, 'missing')) {
                            value.promo_image2_url_abs = "https://placehold.it/1600x800/757575";
                        }
                        
                        temp_promo.push(value);
                    });
                    _.sortBy(temp_promo, [function(o) { return o.start_date; }]);
                    return temp_promo;
                },
            },
            methods: {
                loadData: async function() {
                    try {
                        let results = await Promise.all([this.$store.dispatch("getData", "repos"), this.$store.dispatch("getData", "promotions")]);
                    } catch (e) {
                        console.log("Error loading data: " + e.message);
                    }
                },
                handleButton: function () {
                    if(!this.moreEventsFetched){
                        this.moreEvents = this.promotions;
                        this.events = this.moreEvents.splice(0, 3);
                        this.moreEventsFetched = true;
                    } else {
                        var nextEvents = this.moreEvents.splice(0, 3);
                        // Add 3 more posts to posts array
                        var vm = this;
                        _.forEach(nextEvents, function(value, key) {
                            vm.events.push(value);
                        });
                    }
                    if(this.promotions.length === 0){
                        this.noMoreEvents = true
                        this.noEvents = true
                    } else {

                    }
                }
            }
        });
    });
</script>